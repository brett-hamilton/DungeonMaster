namespace DungeonMaster.Data
{
    /// <summary>
    /// Enum representing what type of weapon it is.
    /// </summary>
    public enum WeaponType
    {
        /// <summary>
        /// One handed weapon. (Club, Mace, etc.)
        /// </summary>
        OneHanded,
        /// <summary>
        /// Two handed weapon. (Spear, Quarterstaff, etc.)
        /// </summary>
        TwoHanded,
        /// <summary>
        /// Light one handed weapon. (Brass Knuckles, Dagger, etc.)
        /// </summary>
        LightOneHanded,
        /// <summary>
        /// Ranged weapon. (Bow, Crossbow, etc.)
        /// </summary>
        Range
    }
}

